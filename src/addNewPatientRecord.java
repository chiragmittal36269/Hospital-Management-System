    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        
        String id = txtid.getText();
        String firstname = txtfirstname.getText();
        String lastname = txtlastname.getText();
        String contact = txtcontact.getText();
        String age = txtage.getText();
        String bloodgroup = txtbloodgroup.getText();
        
        String address = txtaddress.getText();
        String major = txtmajor.getText();
        
        String gender;
        if(r1.isSelected())
        {
            gender = "Male";
        }
        else
        {
            gender = "Female";
        }        
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/hospitaldatabase","root","Chir@g244724");
            pst = con.prepareStatement("insert into addnewpatient(id, firstname, lastname, contact, age, gender, bloodgroup, address, anymajordisease)values(?,?,?,?,?,?,?,?,?)");
            pst.setString(1, id);
            pst.setString(2,firstname);
            pst.setString(3,lastname);
            pst.setString(4,contact);
            pst.setString(5,age);
            pst.setString(6,gender);
            pst.setString(7,bloodgroup);
            pst.setString(8,address);
            pst.setString(9,major);
            
            pst.executeUpdate();
            this.hide();
            JOptionPane.showMessageDialog(null, "Patient Added Successfully.....");
            
            
            
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(addNewPatientRecord.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

